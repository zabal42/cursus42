#include <unistd.h>

void	ft_print_comb2(void)
{
	char	numbers[5];
	int		n1;
	int		n2;

	n1 = 0;
	while (n1 <= 98)
	{
		n2 = n1 + 1;
		while (n2 <= 99)
		{
			numbers[0] = '0' + (n1 / 10);
			numbers[1] = '0' + (n1 % 10);
			numbers[2] = ' ';
			numbers[3] = '0' + (n2 / 10);
			numbers[4] = '0' + (n2 % 10);
			write(1, numbers, 5);
			if (n1 < 98 || n2 < 99)
				write(1, ", ", 2);
			n2++;
		}
		n1++;
	}
}
